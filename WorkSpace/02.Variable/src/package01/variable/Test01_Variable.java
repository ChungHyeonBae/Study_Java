package package01.variable;

public class Test01_Variable {
	public static void main(String[] args) {
		int people;
		int dog;
		int flower;
		people = 3;
		dog = 10;
		flower = 25;
		System.out.println(people);
		System.out.println(dog);
		System.out.println(flower);
		System.out.println(people + "   " + dog + "  " + flower);
	}

}
